import { convexTest } from "convex-test";
import { expect, test } from "vitest";
import { api, internal } from "../convex/_generated/api";
import schema from "../convex/schema";

test("createNotificationsForReply creates notification for parent comment author", async () => {
  const t = convexTest(schema);

  const userId = await t.mutation(internal.users.createOrUpdateUser, {
    googleId: "google-abc",
    email: "parent@gmail.com",
    firstName: "Parent",
    anonymousId: "anon-parent",
  });

  const parentId = await t.mutation(api.comments.postComment, {
    listingId: "L1",
    text: "Parent comment",
    authorId: "anon-parent",
    username: "Parent",
    isAutoGenerated: false,
  });

  await t.mutation(internal.users.migrateAnonymousComments, {
    anonymousId: "anon-parent",
    userId,
  });

  const replyId = await t.mutation(api.comments.postComment, {
    listingId: "L1",
    text: "A reply",
    authorId: "anon-other",
    parentId,
    username: "Other",
    isAutoGenerated: false,
  });

  await t.mutation(internal.notifications.createNotificationsForReply, {
    commentId: replyId,
    parentId,
    listingId: "L1",
    authorName: "Other",
    authorId: "anon-other",
  });

  const notifications = await t.query(api.notifications.getNotifications, {
    userId,
  });
  expect(notifications).toHaveLength(1);
  expect(notifications[0].triggerAuthorName).toBe("Other");
  expect(notifications[0].read).toBe(false);
});

