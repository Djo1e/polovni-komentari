test("getUnreadCount returns correct count", async () => {
  const t = convexTest(schema);

  const userId = await t.mutation(internal.users.createOrUpdateUser, {
    googleId: "google-abc",
    email: "user@gmail.com",
    firstName: "User",
    anonymousId: "anon-user",
  });

  const rootId = await t.mutation(api.comments.postComment, {
    listingId: "L1",
    text: "Root",
    authorId: "anon-user",
    username: "User",
    isAutoGenerated: false,
  });

  await t.mutation(internal.users.migrateAnonymousComments, {
    anonymousId: "anon-user",
    userId,
  });

  for (const name of ["A", "B"]) {
    const replyId = await t.mutation(api.comments.postComment, {
      listingId: "L1",
      text: `Reply from ${name}`,
      authorId: `anon-${name}`,
      parentId: rootId,
      username: name,
      isAutoGenerated: false,
    });

    await t.mutation(internal.notifications.createNotificationsForReply, {
      commentId: replyId,
      parentId: rootId,
      listingId: "L1",
      authorName: name,
      authorId: `anon-${name}`,
    });
  }

  const count = await t.query(api.notifications.getUnreadCount, { userId });
  expect(count).toBe(2);
});

