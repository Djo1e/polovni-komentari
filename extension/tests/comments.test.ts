test("getLatestComments includes isDeleted flag from listing", async () => {
  const t = convexTest(schema);

  await t.mutation(api.comments.postComment, {
    listingId: "12345",
    text: "This listing will be deleted",
    authorId: "user-a",
    username: "TestUser",
    isAutoGenerated: false,
  });

  await t.mutation(api.listings.upsertListing, {
    listingId: "12345",
    title: "Opel Astra",
    price: "1.899 €",
    imageUrl: "https://example.com/img.jpg",
    url: "https://www.polovniautomobili.com/auto-oglasi/12345/opel-astra",
  });

  await t.mutation(api.listings.markListingDeleted, { listingId: "12345" });

  const latest = await t.query(api.comments.getLatestComments, {});
  const comment = latest.find((c: any) => c.text === "This listing will be deleted");
  expect(comment?.listing?.isDeleted).toBe(true);
});

test("postComment triggers notification for parent author", async () => {
  const t = convexTest(schema);

  const userId = await t.mutation(internal.users.createOrUpdateUser, {
    googleId: "google-abc",
    email: "parent@gmail.com",
    firstName: "Parent",
    anonymousId: "anon-parent",
  });

  const parentId = await t.mutation(api.comments.postComment, {
    listingId: "L1",
    text: "Parent",
    authorId: "anon-parent",
    username: "Parent",
    isAutoGenerated: false,
  });
  await t.mutation(internal.users.migrateAnonymousComments, {
    anonymousId: "anon-parent",
    userId,
  });

  await t.mutation(api.comments.postComment, {
    listingId: "L1",
    text: "Reply",
    authorId: "anon-other",
    parentId,
    username: "Other",
    isAutoGenerated: false,
  });

  const notifications = await t.query(api.notifications.getNotifications, { userId });
  expect(notifications).toHaveLength(1);
  expect(notifications[0].triggerAuthorName).toBe("Other");
});
