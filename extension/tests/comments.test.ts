test("getComments: deep replies grouped under root with replyingTo metadata", async () => {
  const t = convexTest(schema);

  const rootId = await t.mutation(api.comments.postComment, {
    listingId: "L1",
    text: "Root",
    authorId: "user-a",
    username: "UserA",
    isAutoGenerated: false,
  });

  const replyId = await t.mutation(api.comments.postComment, {
    listingId: "L1",
    text: "Reply to root",
    authorId: "user-b",
    parentId: rootId,
    username: "UserB",
    isAutoGenerated: false,
  });

  await t.mutation(api.comments.postComment, {
    listingId: "L1",
    text: "Reply to reply",
    authorId: "user-c",
    parentId: replyId,
    username: "UserC",
    isAutoGenerated: false,
  });

  const comments = await t.query(api.comments.getComments, { listingId: "L1" });

  // Only 1 top-level comment
  expect(comments).toHaveLength(1);
  expect(comments[0].text).toBe("Root");

  // Both replies grouped flat under root, sorted chronologically
  expect(comments[0].replies).toHaveLength(2);
  expect(comments[0].replies[0].text).toBe("Reply to root");
  expect(comments[0].replies[1].text).toBe("Reply to reply");

  // First reply has no replyingTo (direct child of root)
  expect(comments[0].replies[0].replyingTo).toBeUndefined();

  // Second reply has replyingTo pointing to UserB
  expect(comments[0].replies[1].replyingTo).toEqual({
    username: "UserB",
    commentId: replyId,
  });
});

